#  Python é uma linguagem com tipagem dinamica
#  Tudo que estiver dentro de aspas é chamada de String
print('exemplo')  # O comando com aspas simples tambem reconhece com string
print("texto")  # o comando com aspas duplas reconhece como string

#exemplo
#  print('Esse exemplo é uma 'String' .str')  forma errada
print("Esse exemplo é uma 'String' .str")
print('Esse exemplo é uma "String" .str\n')

print(r'\nO comando \(n) é usado para pular linha\n')  # O comando r serve para dizer que texto nao deve ser executado
print('O comando \(n) é usado para pular linha\n')

#Caractere de scape \
print('Esse exemplo é uma \'String\' .str')  # O comando barra (\) faz com que o interpretador ignore as aspas do meio

